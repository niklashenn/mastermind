# function for mapping color integers to color strings
def numbertocolor(number):
    if(number==0):
        return "yellow"
    if(number==1):
        return "blue"
    if(number==2):
        return "green"
    if(number==3):
        return "brown"
    if(number==4):
        return "purple"
    if(number==5):
        return "orange"